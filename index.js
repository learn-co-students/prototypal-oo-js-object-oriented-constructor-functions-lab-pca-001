class Scooter {
  constructor(year, color, model){
    this.year = year;
    this.color = color;
    this.model = model;
  }
}

class Driver {
  constructor(name, age, experience){
    this.name = name;
    this.age = age;
    this.experience = experience;
  }
}

class PickupLocation {
  constructor(addr, city){
    this.address = addr;
    this.city = city;
  }
}